export function convertToAlpha2(code: string): string {
	// If it's already a 2-letter code, return it
	if (code.length === 2) {
		return code.toUpperCase();
	}

	// If it's a 3-letter code, convert it
	if (code.length === 3) {
		return countryCodeMap[code.toUpperCase()] || code;
	}

	// If it's neither, return the original code
	return code;
}
